package com.keystone.cold.ui.common;

import android.content.Context;
import android.os.Bundle;
import android.view.View;

import androidx.annotation.Nullable;
import androidx.appcompat.app.AppCompatActivity;

import com.keystone.cold.MainApplication;
import com.keystone.cold.Utilities;
import com.keystone.cold.setting.LanguageHelper;

import static com.keystone.cold.setting.LanguageHelper.ENGLISH;
import static com.keystone.cold.ui.fragment.setting.SystemPreferenceFragment.SETTING_LANGUAGE;

public abstract class FullScreenActivity extends AppCompatActivity {

    @Override
    protected void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        View decorView = getWindow().getDecorView();
        decorView.setSystemUiVisibility(
                View.SYSTEM_UI_FLAG_LAYOUT_STABLE
                        | View.SYSTEM_UI_FLAG_LAYOUT_HIDE_NAVIGATION
                        | View.SYSTEM_UI_FLAG_LAYOUT_FULLSCREEN
                        | View.SYSTEM_UI_FLAG_HIDE_NAVIGATION // hide nav bar
                        | View.SYSTEM_UI_FLAG_FULLSCREEN // hide status bar
                        | View.SYSTEM_UI_FLAG_IMMERSIVE);
    }

    @Override
    protected void attachBaseContext(Context newBase) {
        String language = Utilities.getPrefs(MainApplication.getApplication())
                .getString(SETTING_LANGUAGE, ENGLISH);
        super.attachBaseContext(LanguageHelper.updateResources(newBase, language));
    }
}
